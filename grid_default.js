// ================== PAGINATION ==================
function renderPagination() {
    const totalPages = Math.ceil(all_data.length / itemsPerPage);
    const pagination = document.getElementById("pagination");

    let btn = "";

    if (currentPage > 1) {
        btn += `<button onclick="changePage(${currentPage - 1})">Prev</button>`;
    }

    for (let i = 1; i <= totalPages; i++) {
        btn += `<button onclick="changePage(${i})">${i}</button>`;
    }

    if (currentPage < totalPages) {
        btn += `<button onclick="changePage(${currentPage + 1})">Next</button>`;
    }

    pagination.innerHTML = btn;
}

function changePage(page) {
    currentPage = page;
    renderProducts();
}

